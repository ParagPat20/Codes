/**
 * Motor Movement Guide:
 * 
 * C Motors (Coxa - Horizontal Movement):
 * + angle: Motor moves forward (toward front of robot)
 * - angle: Motor moves backward (toward back of robot)
 * 
 * F Motors (Femur - Vertical Movement):
 * + angle: Motor moves upward (leg lifts)
 * - angle: Motor moves downward (leg lowers)
 * 
 * Example:
 * C1.angle += 10; // Move motor C1 forward
 * C1.angle -= 10; // Move motor C1 backward
 * F1.angle += 10; // Lift leg up
 * F1.angle -= 10; // Lower leg down
 */
